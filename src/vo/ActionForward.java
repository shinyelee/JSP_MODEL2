package vo;

//포워딩 정보를 저장할 수 있는 ActionForward 클래스.
public class ActionForward {

	private boolean isRedirect=false; // 포워딩 방식이 저장되는 변수. 값이 false면 디스패치 방식으로, 값이 true면 리다이렉트 방식으로 포워딩.
	private String path=null; // 서블릿에서 요청 처리 후 포워딩될 최종 뷰 페이지 url이 저장되는 변수 정의.
	
	// alt+shift+s -> r(Generate Getters and Setters) -> Select All -> OK.
	public boolean isRedirect() {
		return isRedirect;
	}
	
	public void setRedirect(boolean b) {
		this.isRedirect = b;
	}
	
	public String getPath() {
		return path;
	}
	
	public void setPath(String string) {
		this.path = string;
	}
}